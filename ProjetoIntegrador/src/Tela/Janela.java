    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.addValue(7000, "alunos", "2008");
        dataset.addValue(7800, "alunos", "2009");
        dataset.addValue(8560, "alunos", "2010");
        dataset.addValue(9009, "alunos", "2010");
        dataset.addValue(10988, "alunos", "2011");
        dataset.addValue(11712, "alunos", "2012");
        dataset.addValue(12800, "alunos", "2013");
        dataset.addValue(13235, "alunos", "2014");

        JFreeChart chart = null;
        if (jRadioButton1.isSelected()) {
            chart = ChartFactory.createLineChart(
                    "Alunos Univates - 2008 - 2014",
                    "Anos", "Alunos",
                    dataset,
                    PlotOrientation.VERTICAL,
                    true, true, false);
        } else if (jRadioButton2.isSelected()) {
            chart = ChartFactory.createBarChart3D("Alunos Univates - 2008 - 2014",
                    "Anos", "Alunos", dataset, PlotOrientation.VERTICAL, false, true, false);
            CategoryPlot p = chart.getCategoryPlot();
            p.setRangeGridlinePaint(Color.black);

        }
        ChartPanel cp = new ChartPanel(chart);
        jPanel1.setLayout(new java.awt.BorderLayout());
        jPanel1.add(cp, BorderLayout.CENTER);
        jPanel1.validate();

    }//GEN-LAST:event_jButton2ActionPerformed

