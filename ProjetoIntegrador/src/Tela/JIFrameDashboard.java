    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<Material> lista = new MaterialDAO().ConsultarTodos();
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        for (Material material : lista) {
            dataset.addValue(material.getId(), material.getId(), material.getId());
        }
        
        JFreeChart chart = null;
        chart = ChartFactory.createBarChart3D("Material",
                "N", "S", dataset, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.black);

        ChartPanel cp = new ChartPanel(chart);
        jPanel1.setLayout(new java.awt.BorderLayout());
        jPanel1.add(cp, BorderLayout.CENTER);
        jPanel1.validate();
    }//GEN-LAST:event_jButton1ActionPerformed

